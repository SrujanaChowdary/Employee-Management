                    ps.setString(11,jTextFieldSal.getText());
                    ps.setString(12,jTextFieldAddress.getText());
                    ps.setString(13,jTextFieldEphn.getText());
                    InputStream img=new FileInputStream(new File(imgPth));
                    ps.setBlob(14,img);
