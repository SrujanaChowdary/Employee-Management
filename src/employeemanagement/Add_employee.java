    public boolean verifData() throws ParseException
    {
        if((jTextFieldFName.getText().equals("")&&jTextFieldLName.getText().equals("")) || String.valueOf(jPasswordField1.getPassword()).equals("")){
            JOptionPane.showMessageDialog(null,"One or more fields are empty!");
            return false;
        }
        else if(! String.valueOf(jPasswordField1.getPassword()).equals(String.valueOf(jPasswordField2.getPassword())))
        {
            jLabeler.setText("Passwords Doesn't match!");
            return false;
        }
        else if(imgPth==null)
        {
            JOptionPane.showMessageDialog(null,"No image selected");
            return false;
        }
        else if(jTextFieldID.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null,"ID cannot be empty!");
            return false;
        }
        else if(jTextFieldPost.getText().equals("") || jTextFieldSal.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null,"One or more fields are empty!");
            return false;
        }
        else if(jTextFieldEmail.getText().indexOf(64)==-1)
        {
            JOptionPane.showMessageDialog(null,"Invalid Email!");
            return false;
        }
        else if(Integer.parseInt(String.valueOf(new myFunc().getdate(jTextFieldDOB.getText())))-Integer.parseInt(String.valueOf(new myFunc().getdate(jTextFieldDOH.getText())))<20)
        {
            JOptionPane.showMessageDialog(null,"Invalid Dates!");
            return false;
        }
        else if(jTextFieldAadhar.getText().equals("") || jTextFieldAadhar.getText().length()!=12)
        {
            JOptionPane.showMessageDialog(null,"Invalid Aadhar!");
            return false;
        }
        else
            return true; 
    }
